<?php

namespace Drupal\milken_base\Plugin\Layout;

use Drupal\Core\Form\FormStateInterface;
use Drupal\Core\Layout\LayoutDefault;


/**
 * A custom Layout for Milken "Slides".
 *
 * @Layout(
 *   id = "one_col_full_width",
 *   label = @Translation("Milken One Col Full Width"),
 *   category = @Translation("milken"),
 *   template = "templates/one-col-full-width",
 *   library = "milken/react-component",
 *   default_region = "content",
 *   icon = "images/one-col-full-width.png",
 *   regions = {
 *     "content" = {
 *       "label" = @Translation("React Coponent"),
 *     },
 *   },
 * )
 */
class OneColFullWidth extends LayoutDefault {

  /**
   * @param array $regions
   *
   * @return array
   */
  function build(array $regions) {
    $toReturn = parent::build($regions); // TODO: Change the autogenerated stub
    if (!isset($toReturn['content']['#node'])) {
      $toReturn['content']['#node'] = \Drupal::routeMatch()->getParameter('node');
    }
    return $toReturn;
  }


  /**
   * {@inheritdoc}
   */
  public function buildConfigurationForm(array $form, FormStateInterface $form_state) {
    $form['label'] = [
      '#type' => 'textfield',
      '#title' => $this->t('Label'),
      '#default_value' => $this->configuration['label'],
    ];
    $form['layout_icon'] = [

    ];
    return $form;
  }

  /**
   * {@inheritdoc}
   */
  public function validateConfigurationForm(array &$form, FormStateInterface $form_state) {
  }

  /**
   * {@inheritdoc}
   */
  public function submitConfigurationForm(array &$form, FormStateInterface $form_state) {
    $this->configuration['label'] = $form_state->getValue('label');
  }

}
